//PROGRAM TO PRINT THE SUM OF TWO INPUTTED NUMBERS.
#include <iostream>
using namespace std;
int main ()
{
    int n1, n2, sum;
    cout << endl << "Enter your first number: ";
    cin >> n1;
    cout << "Enter your second number: ";
    cin >> n2;
    cout << "Sum: ";
    sum=n1+n2;
    cout << sum << endl;
    cout << endl;
    return 0;
}